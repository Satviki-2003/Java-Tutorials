public class Tut_08_Operators {
    public static void main(String[] args) {

        // Arithmetic operators
        int a = 4;
        int b = 6%a;// modulo operator

        // Assignment operator
        int c = 9;
        c *=8;

        // Comparison operator
        System.out.println(64>5);

        //Logical operators
        System.out.println(89> 8 && 89<90);
        System.out.println(67>0 || 89<90);

        //Bitwise operator
        System.out.println(2&3);

        //true
        //true
        //true
        //2
    }
}
